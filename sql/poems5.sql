<p class="-9">Заглавие</p>
<p class="-10">Названия стран света в Китае переводчика затрудняют. Не перевести их нельзя, перевод же дает фальшивую ассоциацию с географическими особен ностями. Каждый китаец всегда воспитывался и воспитывается до сих пор в подлинной, а не случайной ориентации. Называют, например, адрес так: к востоку от стены, такая-то улица к западу, в южном тупике, на северной стороне улицы, восточные ворота. Слепому кричат: учитель (из вежливости), восточнее держись! В поэзии часто встречаются: восточное озеро, южный поток, северная гора, которые, в сущности, надо переводить по-русски так: левое озеро, поток по правую руку, гора от второй налево и т.д. Для маленькой речки в китайской поэзии много синонимов, которые не всегда легко передать.</p>
