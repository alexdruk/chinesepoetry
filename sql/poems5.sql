<p class="-3">Введение</p>
<p class="-4">Тема заброшенного дворца, свидетеля былого великолепия, закончившегося разрушением и смертью, - тема для китайской поэзии всех времен обычная, и к ней придется вернуться не раз при переводе этой антологии.</p>
<p class="-9">Заглавие</p>
<p class="-10"><em>Яшмовый</em> по-русски не звучит, но для китайца это эпитет ко всему дорогому, лучшему - как к предметам, так и к отвлеченным понятиям. Так, имеем целый ряд приложений этого эпитета к следующим словам: девица, доблесть, лучший человек, милый, прекрасный человек, весна, лютня [цитра], столица (эмпиреи) [в даосизме: столица Верховного Владыки], чара вина, сосуд и его сравнения, луна, небо, чистое сердце, друг, облик, письмо, лицо, красота и т.д., наконец, царь и бог. "<em>Нефритовый</em>", как ближайший вариант перевода, тоже не спасет положения. Происходит это несоответствие языковых возможностей оттого, что в китайской поэзии яшма обладает мистическими свойствами: она чиста, струиста, тепла и влажна, мягка и тверда, не грязнится, блестит, бела, обладает чистым звуком; влажна, но не потеет; угловата, но не убивает и т.д. Всех этих образов в русской поэзии, а следовательно, и в русском поэтическом языке нет. Здесь <em>яшмовая чистота</em> подразумевает небесную, божественную чистоту, т.е. просто само Небо, покровительствующее царю как в его дворце, так и в храме, основанном впоследствии на его месте.</p>
<p class="-10">Этот царский чертог был построен в 646 г., в самый разгар китайской завоевательной славы в Средней Азии и вообще в пору лучшего расцвета единой империи, идущего после мрачного периода ее дробления и захвата китайской земли инородцами (хотя и танская династия была основана китаизированным тюрком).</p>
<p class="-10"><em>Дворец</em> стоял в нынешней Шааньси (Шэньси), недалеко от столицы. Он был дворцом недолго и уже в 651 г. расформирован в буддийский храм Юйхуасы, потом разграблен и разрушен.</p>
