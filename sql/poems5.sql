<p class="-7"><strong><span style="font-size:0.67em; "><sup>1</sup></span></strong><em>Тысяч десятки свирелей</em> - образ, взятый из мистики Чжуан-цзы (IV в.</p>
<p class="-7">до н.э.) и живописно передающий этими словами все звуки природы вокруг человека<strong><span style="font-size:0.67em; "><sup>IV</sup></span></strong>.</p>
<p class="-7"><strong><span style="font-size:0.67em; "><sup>2</sup></span></strong><em>Осенние краски</em> - непередаваемый образ <em>сяо-са</em>; может еще говорить о просторах, открывающихся осенью после парного китайского лета с его слякотью и парной мутью.</p>
<p class="-7"><strong><span style="font-size:0.67em; "><sup>3</sup></span></strong> Они [красавицы] "прислуживали" государю во всех его поездках.</p>
<p class="-7"><strong><span style="font-size:0.67em; "><sup>4</sup></span></strong> На могилах императоров ставились изваяния животных и людей - памятники его славы. Особенно хороши были барельефы шести коней на могиле императора Тай-цзуна, умершего в 650 г. О них существует целая литература как на китайском, так и на японском и европейских языках.</p>
