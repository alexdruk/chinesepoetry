<p class="a33">Возглавлял шествие безукоризненно одетый человек средних лет, он спрыгнул с повозки, непринужденно подошел к циньским военачальникам, очень гостеприимно поклонился им и сказал: - Я посланец царства Чжэн, Сянь Гао. Говорят, что армия вашей страны собирается к нам в гости, наш правитель боится, принимая вас, что-нибудь упустить из виду, он послал меня вам навстречу. Кроме того, правитель беспокоится, не устали ли вы с дороги, хватает ли вам провианта, поэтому он приказал передать вам в дар этих быков для пополнения запасов.</p>
<p class="a33">Предводитель циньского войска выслушал посланника и подумал, что дело плохо: "О Небо, кажется, наш тайный поход обнаружили! Это как же слухи разнеслись! Даже правитель Чжэн послал человека следить за нашим перемещением, как нам теперь поступить?" Мэн Минши сжал рукоятку меча и пристально взглянул на Сянь Гао. Однако тот не смутился и громко приказал помощнику пригнать быков. - Будь любезен, когда вернешься, передай своему правителю благодарность за его доброту, - натужно улыбнулся очень недовольный Сици Шу.</p>
<p class="a33">Обе стороны церемонно обменялись несколькими фразами и попрощались. Когда встреча закончилась, циньские командующие принялись обсуждать дальнейшие действия, а Сянь Гао срочно послал гонца сообщить новому правителю царства Чжэн новость о вторжении врагов: дело в том, что Сянь Гао вовсе не был посланником, он был торговцем из царства Чжэн и как раз направлялся в царство Хуа, чтобы продать своих быков.</p>
<p class="a33">По дороге он случайно встретил войско царства Цинь. Сянь Гао сразу понял, что царство Цинь намеревается воспользоваться трауром в Чжэн и атаковать его. Он был патриотом, но было понятно, что силой одного человека не преградить путь циньской армии. Тогда он решил выдать себя за посланника, сначала успокоить циньское войско, а потом известить своего правителя о готовящемся нападении.</p>
<p class="a33">Правитель царства Чжэн получил тайное сообщение и привел войска в боевую готовность. Военачальники армии Цинь поняли, что неожиданно напасть не удастся, отозвали войска и вернулись домой.</p>
<p class="a33">Торговец Сянь Гао благодаря находчивости, невозмутимости и преданности своему царству сумел сделать так, чтобы царство Чжэн избежало порабощения,</p>
<p class="a33" style="text-align:center">КАК ШАН ЯН ЗАВОЕВЫВАЛ НАРОДНОЕ ДОВЕРИЕ</p>
<p class="a33">Период Весен и Осеней и Сражающихся царств в истории Китая - это тяжелые смутные времена; тогда выдающиеся личности объединялись, князья сражались за господство, обстановка была непредсказуемой. В таких условиях стремящиеся к гегемонии князья в каждом царстве обязательно должны были проводить реформы. Министр царства Цинь, Шан Ян, разработал ряд законов. Он был очень уверен в себе и считал, что его реформы сделают царство Цинь сильнее.</p>
<p class="a33">Однако тут же возникли проблемы. Прежние указы действовали уже много лет, народ привык к ним. К тому же Шан Ян боялся, что люди не поверят в его твердую решимость ввести в действие новые законы. Поэтому перед их обнародованием Шан Ян должен был доказать, что он надежный человек и ему можно доверять.</p>
<p class="a33">Он распорядился установить у южных ворот города деревянный столб высотой в три чжана и объявить народу, что тот, кто перенесет столб к северным воротам, получит награду в десять лянов2 золота.</p>
<p class="a33">Народ обсуждал эту задачу и спорил, но никто не хотел пробовать. От южных до северных ворот было не очень далеко, к тому же деревянный столб в три чжана хоть не легок, но точно не очень тяжел. Иными словами, задача эта нисколько не трудная, а десять лянов золота, напротив, очень большая сумма. Что-то здесь не чисто, может, это какая-то ловушка?</p>
<p class="a33">Притом Шан Ян такой могущественный человек, если он обманет или пошутит с тобой, то никуда и не пойдешь за правдой!</p>
<p class="a33">Шан Ян посмотрел на это, разволновался и увеличил награду до пятидесяти лянов золота. Тут уж люди еще больше засомневались, однако наконец один дюжий мужчина не удержался перед соблазном получить пятьдесят лянов золота. Он подумал, что ради таких денег- обманут или нет- надо попробовать. Окруженный толпой, он легко перенес столб от южных ворот к северным. Шан Ян действительно не обманул и тут же отдал этому молодцу обещанную награду! "И вправду дал!", "Шан Ян и впрямь делает то, что говорит", "Ах, как жалко! Я тоже мог перенести!", "Это все я виноват, что не верил ему! Иначе я бы тоже мог получить эти деньги".</p>
<p class="a33">Люди без конца изумлялись, сожалели об упущенной возможности и одновременно начинали верить Шан Яну.</p>
<p class="a33">Шан Ян приобрел авторитет среди народа, теперь ему намного сподручнее было проводить политические реформы.</p>
<p class="a33">Люди знали, что он держит свое слово, поэтому не смели бросать вызов новым законам.</p>
<p class="a33">Однако некоторые знатные и влиятельные люди тогда подумали: "Реформы Шан Яна только для простолюдинов; если мы, высокопоставленные чиновники, пойдем против закона, наверняка он не посмеет ничего сделать. Ему придется выказывать нам уважение". Среди этих людей неожиданно оказался и наследный принц! Он и впрямь нарушил новый закон.</p>
<p class="a33">Как же тут поступить? Принц нарушил закон, если его наказать, то кто же потом наследует престол? Если его не наказывать, как же вернуть народу Поднебесной веру в силу и действиеновых законов? Несмотря на большой риск, Шан Ян сурово наказал учителей принца и тех чиновников, которые подали ему идею.</p>
<p class="a33">Народ увидел, что даже учителя принца не могут избежать наказания по новым законам, и больше никто не решался нарушать их. Царство Цинь после проведения реформ крепло и процветало.</p>
<p class="a33" style="text-align:center">НЕ РВИТЕ СЛИВЫ У ДОРОГИ</p>
<p class="a33">Однажды солнечным днем ватага ребят веселилась за городом. От хохота и возни птицы в лесу всполошились и улетели.</p>
<p class="a33">Дети вышли к широкой дороге, они уже несколько подустали, и один парнишка воскликнул: - Я так хочу пить! Вот бы нам встретился ручеек! - Вот бы найти сочные фрукты! Можно и жажду утолить, и полакомиться,- добавил другой, сглотнув слюну, и все рассмеялись.</p>
<p class="a33">Вдруг один из ребят закричал: - Смотрите! Сливовое дерево!</p>
<p class="a33">Все обернулись в ту сторону, куда он указывал, и действительно увидели высокое сливовое дерево, росшее у дороги.</p>
<p class="a33">И все оно было усыпано красными спелыми сливами!</p>
<p class="a33">Дети воодушевились и с радостными криками помчались к сливовому дереву. Плоды сверкали на солнце, как агаты.</p>
<p class="a33">Однако один мальчик продолжал невозмутимо стоять, как будто аппетитные фрукты ничуть его не привлекали.</p>
<p class="a33">Приятели оставили его далеко позади, никто уже не обращал внимания на его странное поведение, Лишь один прохожий заметил это, подошел к нему и спросил: - Малыш, разве ты не любишь сливы? - Конечно, люблю, - последовал ответ. - Тогда почему ты не идешь их собирать вместе с товарищами? - удивился прохожий.</p>
<p class="a33">Мальчик улыбнулся и тоже задал вопрос: - Это большое сливовое дерево, к тому же ничье, оно растет у дороги. Как, по-вашему, здесь часто кто-нибудь ходит?</p>
<p class="a33">- Конечно, это главная дорога, я часто хожу по ней, - сказал прохожий. - Почему же дерево сплошь покрыто сливами, отчего никто их не срывает?- продолжал мальчик. - Точно! Почему же? - прохожий был озадачен. - Думаю, плоды у этого дерева или горькие, или кислые!</p>
<p class="a33">И правда, только что с азартом забравшиеся на дерево и сорвавшие сливы ребята рассерженно и разочарованно крикнули стоявшим внизу товарищам: - Сливы горькие!</p>
<p class="a33">Прохожий изумился, он и подумать не мог, что этот малыш так смышлен.</p>
<p class="a33">Ребенка звали Ван Жун, а когда он вырос, то стал одним из семи мудрецов из Бамбуковой рощи периода Вэй-Цзинь (220-420 гг. н.э.).</p>
