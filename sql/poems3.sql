<p class="a2">Перевод: <a href="./translators.php\?record_id=62" class="translators ref">Ма Хайпэн</a>, <a href="./translators.php\?record_id=88" class="translators ref">Пэн Сюэ</a>, <a href="./translators.php\?record_id=114" class="translators ref">Цяо Жуйлин</a></p>
<p class="a34">Высокие горы, текущие воды</p>
<p class="a33">В период Чуньцю жил человек, которого звали Юй Боя, в молодости он был очень умен и любознателен, играл на цитре и учился этому у выдающегося музыканта, поэтому был настоящим мастером этого дела. Но все же он был недоволен своей техникой исполнения. Когда об этих переживаниях и сомнениях Юй Боя узнал его учитель, он взял юношу с собой в плавание по Восточно-Китайскому морю на остров Пэнлай, чтобы тот насладился прекрасными картинами природы и вслушался в плеск морских волн.</p>
<p class="a33">Перед глазами Юй Боя вздымались волны, летали птицы, чьи песни ласкали слух. Горы, покрытые лесами, цветы и травы вокруг - юноша словно попал на сказочный остров. В ушах его зазвучала гармоничная музыка природы. Не в силах сдерживаться, юноша взял инструмент и стал играть, мелодия лилась сама по себе, красота природы сплелась со звуками цитры.</p>
<p class="a33">Юй Боя постиг неизведанные ранее границы, и учитель удовлетворенно сказал, что теперь-то он всему научился.</p>
<p class="a33">Однажды Юй Боя отправился на лодочную прогулку, но вдруг налетела страшная буря, и команда быстро увела лодку в укрытие под скалу. Когда ливень утих, Юй Боя почувствовал, что среди гор звучит особая волнующая мелодия, и он тут же заиграл на цитре.</p>
<p class="a33">Вдруг струна порвалась. Он поднял голову и увидел на обрыве дровосека, который внимательно его слушал.</p>
<p class="a33">Юй Боя был удивлен и громко крикнул: - Позвольте спросить, как вас зовут? И как же вы здесь оказались?</p>
<p class="a33">Дровосек ответил: - Ваш покорный слуга, Чжун Цзыци, я заготавливал дрова, и дождь здесь меня задержал. Внезапно я услышал звуки цитры и невольно заслушался!</p>
<p class="a33">Юй Боя обрадовался: - Раз уж ты слушал, то можешь ли сказать, что за мелодию я только что играл? - То, что вы только что играли, - это восторг от увиденного пейзажа, гор после дождя, цитра звучала именно так!- ответил Чжун Цзыци.- А еще я услышал в вашей мелодии звук бегущей среди гор реки.</p>
<p class="a33">Юй Боя был потрясен и взволнованно сказал: - Ты понимаешь музыку! Ты мой настоящий друг! Мелодия, которую я только что играл, пусть называется "Высокие горы, текущие воды".</p>
<p class="a33">Сказав это, он жестом пригласил Чжун Цзыци поклониться зеленым горам.</p>
<p class="a33">С тех пор они стали лучшими друзьями. Когда же Чжун Цзыци умер, Юй Боя был подавлен горем и разбил свою любимую цитру перед могилой Чжун Цзыци со словами: - Близкий друг умер, как же могу я играть?</p>
